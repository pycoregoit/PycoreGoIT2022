""" Our first lesson """

# Int
number = 2
print(number)

print(type(number))

# Float
number_with_coma = 2.5
print(number_with_coma)
print(type(number_with_coma))

# String
name = "Alisa"
print(name)
print(type(name))

# List
name_of_list = ["Alisa", "Bob"]
print(name_of_list)
print(type(name_of_list))

# Tuple
name_of_tuples = ("Alisa", "Bob")
print(name_of_tuples)
print(type(name_of_tuples))

# Dict
dictionary = {"name": "Alisa", "surname": "Nikols"}
print(dictionary)
print(type(dictionary))

# Set
unique = {1, 2, 3, 3}
print(unique)
print(type(unique))

# Bool
boolean_true = True
boolean_false = False
print(boolean_true)
print(type(boolean_true))
print(boolean_false)
print(type(boolean_false))

